package com.sbiao360.cms.domain;

public class RankingData {
	
	private String id;
	
	private Integer countNow;
	
	private Integer countIncre;
	
	private String name;
	
	private Integer pm;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public Integer getCountNow() {
		return countNow;
	}

	public void setCountNow(Integer countNow) {
		this.countNow = countNow;
	}

	public Integer getCountIncre() {
		return countIncre;
	}

	public void setCountIncre(Integer countIncre) {
		this.countIncre = countIncre;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public Integer getPm() {
		return pm;
	}

	public void setPm(Integer pm) {
		this.pm = pm;
	}

}
